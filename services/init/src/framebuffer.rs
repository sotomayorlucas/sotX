/// Return the current console dimensions: (cols, rows, xpixel, ypixel).
/// If the framebuffer is not active, returns the serial default (80x24).
pub(crate) fn con_dimensions() -> (u16, u16, u16, u16) {
    unsafe {
        if !*FB_ACTIVE.get() {
            return (80, 24, 0, 0);
        }
        let cols = *CON_COLS.get() as u16;
        let rows = *CON_ROWS.get() as u16;
        let xpixel = (cols as u32 * 8) as u16;
        let ypixel = (rows as u32 * 16) as u16;
        (cols, rows, xpixel, ypixel)
    }
}

/// Buffered byte from serial peek.
static KB_PEEK_BUF: AtomicU64 = AtomicU64::new(0);

/// Check if a character is available without consuming it.
pub(crate) unsafe fn kb_has_char() -> bool {
    if KB_PEEK_BUF.load(Ordering::Relaxed) != 0 { return true; }
    if let Some(b) = sys::debug_read() {
        let ch = if b == b'\r' { b'\n' } else { b };
        KB_PEEK_BUF.store(ch as u64, Ordering::Relaxed);
        return true;
    }
    let ring = KB_RING_ADDR as *mut u32;
    let write_idx = core::ptr::read_volatile(ring);
    let read_idx = core::ptr::read_volatile(ring.add(1));
    read_idx != write_idx
}

pub(crate) unsafe fn kb_read_char() -> Option<u8> {
    let peeked = KB_PEEK_BUF.swap(0, Ordering::Relaxed);
    if peeked != 0 {
        return Some(peeked as u8);
    }
    if let Some(b) = sys::debug_read() {
        return Some(if b == b'\r' { b'\n' } else { b });
    }

    let ring = KB_RING_ADDR as *mut u32;
    loop {
        let write_idx = core::ptr::read_volatile(ring);
        let read_idx = core::ptr::read_volatile(ring.add(1));
        if read_idx == write_idx {
            return None;
        }
        let scancode = *((KB_RING_ADDR + 8 + (read_idx & 0xFF) as u64) as *const u8);
        let new_read = (read_idx + 1) & 0xFF;
        core::ptr::write_volatile(ring.add(1), new_read);

        match scancode {
            0x2A | 0x36 => { *KB_SHIFT.get() = true; continue; }
            0xAA | 0xB6 => { *KB_SHIFT.get() = false; continue; }
            0x1D => { *KB_CTRL.get() = true; continue; }
            0x9D => { *KB_CTRL.get() = false; continue; }
            _ => {}
        }

        // Shift+PgUp / Shift+PgDn — scroll back/forward through history.
        // PS/2 Set 1: PgUp = 0x49, PgDn = 0x51.
        if *KB_SHIFT.get() && scancode == 0x49 {
            sb_scroll_up(5);
            continue;
        }
        if *KB_SHIFT.get() && scancode == 0x51 {
            sb_scroll_down(5);
            continue;
        }

        if scancode & 0x80 != 0 {
            continue;
        }

        let ascii = if *KB_SHIFT.get() {
            SCANCODE_TO_ASCII_SHIFT[scancode as usize]
        } else {
            SCANCODE_TO_ASCII[scancode as usize]
        };

        if ascii == 0 { continue; }

        if *KB_CTRL.get() {
            if ascii >= b'a' && ascii <= b'z' {
                return Some(ascii - b'a' + 1);
            }
            if ascii >= b'A' && ascii <= b'Z' {
                return Some(ascii - b'A' + 1);
            }
        }

        return Some(ascii);
    }
}
