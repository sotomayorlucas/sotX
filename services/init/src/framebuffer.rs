// Current SGR style (updated by `\x1b[...m` sequences, read by the glyph
// drawer). Kept as plain cells — the console is single-threaded, owned by
// init, and all writers already go through `fb_putchar`.
// ---------------------------------------------------------------------------
static CUR_FG: SyncUnsafeCell<u32> = SyncUnsafeCell::new(DEFAULT_FG);
static CUR_BG: SyncUnsafeCell<u32> = SyncUnsafeCell::new(DEFAULT_BG);
static CUR_BOLD: SyncUnsafeCell<bool> = SyncUnsafeCell::new(false);
static CUR_REVERSE: SyncUnsafeCell<bool> = SyncUnsafeCell::new(false);

/// Resolve the current effective (fg, bg) taking bold and reverse into
/// account. Bold upgrades the standard ANSI base colors to their bright
/// variant (xterm-compatible); truecolor/256-color values are left
/// alone. Reverse simply swaps fg and bg.
#[inline]
unsafe fn current_colors() -> (u32, u32) {
    let mut fg = *CUR_FG.get();
    let bg = *CUR_BG.get();
    if *CUR_BOLD.get() {
        // Upgrade base ANSI colors 0..=7 to their bright counterparts.
        for i in 0..8 {
            if fg == ANSI_PALETTE[i] {
                fg = ANSI_PALETTE[i + 8];
                break;
            }
        }
    }
    if *CUR_REVERSE.get() { (bg, fg) } else { (fg, bg) }
}

// ---------------------------------------------------------------------------
// Scrollback ring buffer (256 rows x 200 cols)
// ---------------------------------------------------------------------------
const SB_ROWS: usize = 256;
const SB_COLS: usize = 200;

/// A single cell in the scrollback buffer.
#[derive(Clone, Copy)]
#[repr(C)]
struct ScrollbackCell {
    ch: u8,
    _pad: u8,
}

impl ScrollbackCell {
    const fn empty() -> Self { Self { ch: b' ', _pad: 0 } }
}

/// Scrollback ring: SB_ROWS rows of SB_COLS cells each.
static mut SB_RING: [[ScrollbackCell; SB_COLS]; SB_ROWS] =
    [[ScrollbackCell::empty(); SB_COLS]; SB_ROWS];
/// Write head in the ring (next row to write into).
static mut SB_HEAD: usize = 0;
/// Number of valid rows stored (up to SB_ROWS).
static mut SB_COUNT: usize = 0;
/// Current scroll-back view offset (0 = live, >0 = scrolled back N rows).
static mut SB_VIEW_OFFSET: usize = 0;
/// Screen shadow buffer — mirrors the visible character cells so we can
/// evict rows into the scrollback ring and redraw on scroll-back.
/// Max 80 rows (generous for any realistic resolution).
const SB_SCREEN_MAX_ROWS: usize = 80;
static mut SB_SCREEN_BUF: [[ScrollbackCell; SB_COLS]; SB_SCREEN_MAX_ROWS] =
    [[ScrollbackCell::empty(); SB_COLS]; SB_SCREEN_MAX_ROWS];

