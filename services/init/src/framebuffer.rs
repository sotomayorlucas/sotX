// Print helpers — serial only when no framebuffer, serial+fb when available
// ---------------------------------------------------------------------------

/// Whether the framebuffer is active (set in fb_init).
static FB_ACTIVE: SyncUnsafeCell<bool> = SyncUnsafeCell::new(false);

/// Suspend init's framebuffer rendering so a downstream owner (typically the
/// Wayland compositor) can take over exclusively. After this call every
/// `fb_putchar` / `fb_draw_glyph_at` path becomes a no-op; subsequent text
/// output still reaches serial via `sys::debug_print` but no longer races
/// the compositor's repaint cycle.
///
/// Idempotent: safe to call many times. No way to resume other than a
/// reboot — once the compositor is up we don't want two renderers touching
/// the framebuffer again.
pub fn suspend() {
    unsafe { *FB_ACTIVE.get() = false; }
}

pub(crate) fn print(s: &[u8]) {
    for &b in s {
        sys::debug_print(b);
    }
    if unsafe { *FB_ACTIVE.get() } {
        // Drain any pending kernel messages first so they appear in order.
        unsafe { drain_console_ring(); }
        for &b in s {
            unsafe { fb_putchar(b); }
        }
    }
}

pub(crate) fn print_u64(mut n: u64) {
    if n == 0 {
        sys::debug_print(b'0');
        if unsafe { *FB_ACTIVE.get() } { unsafe { fb_putchar(b'0'); } }
        return;
    }
    let mut buf = [0u8; 20];
    let mut i = 0;
    while n > 0 {
        buf[i] = b'0' + (n % 10) as u8;
        n /= 10;
        i += 1;
    }
    while i > 0 {
        i -= 1;
        sys::debug_print(buf[i]);
        if unsafe { *FB_ACTIVE.get() } { unsafe { fb_putchar(buf[i]); } }
    }
}

pub(crate) fn print_hex64(mut n: u64) {
    if n == 0 {
        sys::debug_print(b'0');
        if unsafe { *FB_ACTIVE.get() } { unsafe { fb_putchar(b'0'); } }
        return;
    }
    let mut buf = [0u8; 16];
    let mut i = 0;
    while n > 0 {
        let digit = (n & 0xF) as u8;
        buf[i] = if digit < 10 { b'0' + digit } else { b'a' + digit - 10 };
        n >>= 4;
        i += 1;
    }
    while i > 0 {
        i -= 1;
        sys::debug_print(buf[i]);
        if unsafe { *FB_ACTIVE.get() } { unsafe { fb_putchar(buf[i]); } }
    }
}

pub(crate) fn print_hex8(val: u8) {
    let hex = b"0123456789ABCDEF";
    let hi = hex[((val >> 4) & 0xF) as usize];
    let lo = hex[(val & 0xF) as usize];
    sys::debug_print(hi);
    sys::debug_print(lo);
    if unsafe { *FB_ACTIVE.get() } {
        unsafe { fb_putchar(hi); fb_putchar(lo); }
    }
}

pub(crate) fn print_hex(val: u32) {
    let hex = b"0123456789ABCDEF";
    for i in (0..8).rev() {
        sys::debug_print(hex[((val >> (i * 4)) & 0xF) as usize]);
    }
}

// ---------------------------------------------------------------------------
// Kernel console ring drain — replay kernel kprintln! output on framebuffer
// ---------------------------------------------------------------------------

/// Drain all pending bytes from the kernel console ring buffer and feed them
/// through `fb_putchar` so kernel boot messages appear on the framebuffer.
pub(crate) unsafe fn drain_console_ring() {
    if !*FB_ACTIVE.get() {
        return;
    }
    let base = CONSOLE_RING_ADDR as *mut u32;
    let data = (CONSOLE_RING_ADDR + 8) as *const u8;

    loop {
        let write_idx = core::ptr::read_volatile(base);
        let read_idx = core::ptr::read_volatile(base.add(1));
        if read_idx == write_idx {
            break;
        }
        let byte = core::ptr::read_volatile(data.add(read_idx as usize));
        let next = (read_idx + 1) % CONSOLE_RING_CAP;
        core::ptr::write_volatile(base.add(1), next);
        fb_putchar(byte);
    }
}

