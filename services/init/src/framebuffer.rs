/// Evict the top visible row (row 0) into the scrollback ring.
/// Called just before the framebuffer pixel data is shifted up.
unsafe fn sb_evict_top_row() {
    let cols = (*CON_COLS.get() as usize).min(SB_COLS);
    // We cannot directly read characters back from the framebuffer pixels.
    // Instead we keep a simple character-cell shadow buffer for row 0.
    // Because fb_scroll shifts rows 1..N into 0..N-1, the data that was
    // in screen row 0 is the one being lost.  We sample it from
    // SB_SCREEN_BUF[0] which fb_draw_char and print path maintain.
    let dst = &mut SB_RING[SB_HEAD];
    for c in 0..cols {
        dst[c] = SB_SCREEN_BUF[0][c];
    }
    for c in cols..SB_COLS {
        dst[c] = ScrollbackCell::empty();
    }
    SB_HEAD = (SB_HEAD + 1) % SB_ROWS;
    if SB_COUNT < SB_ROWS { SB_COUNT += 1; }

    // Shift the screen shadow buffer up by one row.
    let rows = *CON_ROWS.get() as usize;
    for r in 0..(rows.saturating_sub(1)) {
        SB_SCREEN_BUF[r] = SB_SCREEN_BUF[r + 1];
    }
    if rows > 0 {
        SB_SCREEN_BUF[rows - 1] = [ScrollbackCell::empty(); SB_COLS];
    }
}

/// Redraw the entire visible area from the scrollback ring at the current
/// `SB_VIEW_OFFSET`.  Offset 0 = live screen, offset N = N rows back.
unsafe fn sb_redraw_view() {
    let cols = (*CON_COLS.get() as usize).min(SB_COLS);
    let rows = *CON_ROWS.get() as usize;
    let offset = SB_VIEW_OFFSET;

    if offset == 0 {
        // Redraw from the live screen buffer.
        for r in 0..rows {
            for c in 0..cols {
                let ch = SB_SCREEN_BUF[r][c].ch;
                fb_draw_char(c as u32, r as u32, ch);
            }
        }
        return;
    }

    // Drawing from the scrollback ring.
    // The ring stores the most recent SB_COUNT evicted rows.
    // SB_HEAD points one past the newest entry.
    // offset=1 means the most recent evicted row is the last visible row,
    // with older rows above it.
    for vr in 0..rows {
        // Which scrollback row does screen row `vr` correspond to?
        // The bottommost visible row when fully scrolled back by `offset`
        // is the (offset-1)th evicted row (counting from newest=0).
        let sb_age = offset as isize - vr as isize; // age from newest
        if sb_age < 1 || sb_age as usize > SB_COUNT {
            // This row is either beyond stored history or on the live screen
            // For rows that would be on the live screen, show the screen buf.
            let live_row = vr as isize - offset as isize;
            if live_row >= 0 && (live_row as usize) < rows {
                for c in 0..cols {
                    let ch = SB_SCREEN_BUF[live_row as usize][c].ch;
                    fb_draw_char(c as u32, vr as u32, ch);
                }
            } else {
                // Beyond history — blank
                for c in 0..cols {
                    fb_draw_char(c as u32, vr as u32, b' ');
                }
            }
        } else {
            // Read from scrollback ring. age=1 is newest (at SB_HEAD-1).
            let idx = (SB_HEAD + SB_ROWS - sb_age as usize) % SB_ROWS;
            for c in 0..cols {
                let ch = SB_RING[idx][c].ch;
                fb_draw_char(c as u32, vr as u32, ch);
            }
        }
    }
}

/// Scroll the view backward (into history) by N rows.
pub(crate) unsafe fn sb_scroll_up(n: usize) {
    let max = SB_COUNT;
    SB_VIEW_OFFSET = (SB_VIEW_OFFSET + n).min(max);
    sb_redraw_view();
}

/// Scroll the view forward (toward live) by N rows.
pub(crate) unsafe fn sb_scroll_down(n: usize) {
    SB_VIEW_OFFSET = SB_VIEW_OFFSET.saturating_sub(n);
    sb_redraw_view();
}

/// Scroll the text console up by one row.
unsafe fn fb_scroll() {
    // Evict the top row into the scrollback ring before it is overwritten.
    sb_evict_top_row();

    let fb_u8 = *FB_PTR.get() as *mut u8;
    let pitch = *FB_PITCH.get() as usize;
    let fb32 = *FB_PTR.get() as *mut u32;
    let stride = *FB_PITCH.get() / 4;
    let text_width_bytes = (*CON_COLS.get() * 8 * 4) as usize;
    let text_x_bytes = (*TEXT_X.get() * 4) as usize;

    for row in 0..(*CON_ROWS.get() - 1) {
        for py in 0..16u32 {
            let src_y = (*TEXT_Y.get() + (row + 1) * 16 + py) as usize;
            let dst_y = (*TEXT_Y.get() + row * 16 + py) as usize;
            let src = fb_u8.add(src_y * pitch + text_x_bytes);
            let dst = fb_u8.add(dst_y * pitch + text_x_bytes);
            core::ptr::copy(src, dst, text_width_bytes);
        }
    }
    let last_row = *CON_ROWS.get() - 1;
    for py in 0..16u32 {
        let y = (*TEXT_Y.get() + last_row * 16 + py) as usize;
        for col in 0..*CON_COLS.get() * 8 {
            let x = *TEXT_X.get() as usize + col as usize;
            *fb32.add(y * stride as usize + x) = COL_BG;
        }
    }
}

// ---------------------------------------------------------------------------
// ANSI escape sequence parser (vte crate)
// ---------------------------------------------------------------------------
static VTE_PARSER: SyncUnsafeCell<Option<vte::Parser>> = SyncUnsafeCell::new(None);
static ANSI_SAVED_COL: SyncUnsafeCell<u32> = SyncUnsafeCell::new(0);
static ANSI_SAVED_ROW: SyncUnsafeCell<u32> = SyncUnsafeCell::new(0);

/// Clear a rectangular region of the text console.
unsafe fn fb_clear_region(col_start: u32, col_end: u32, row_start: u32, row_end: u32) {
    let fb32 = *FB_PTR.get() as *mut u32;
    let stride = *FB_PITCH.get() / 4;
    for row in row_start..row_end {
        for py in 0..16u32 {
            let y = *TEXT_Y.get() + row * 16 + py;
            for col in col_start..col_end {
                for px in 0..8u32 {
                    let x = *TEXT_X.get() + col * 8 + px;
                    *fb32.add(y as usize * stride as usize + x as usize) = COL_BG;
                }
            }
        }
        // Clear screen shadow buffer for this row.
        let r = row as usize;
        if r < SB_SCREEN_MAX_ROWS {
            for c in (col_start as usize)..(col_end as usize).min(SB_COLS) {
                SB_SCREEN_BUF[r][c] = ScrollbackCell::empty();
            }
        }
    }
}

struct FbPerformer;

impl vte::Perform for FbPerformer {
    fn print(&mut self, c: char) {
        unsafe {
            let ch = if (c as u32) <= 0xFF { c as u8 } else { b'?' };
            fb_draw_char(*CON_CUR_COL.get(), *CON_CUR_ROW.get(), ch);
            *CON_CUR_COL.get() += 1;
            if *CON_CUR_COL.get() >= *CON_COLS.get() {
                *CON_CUR_COL.get() = 0;
                *CON_CUR_ROW.get() += 1;
                if *CON_CUR_ROW.get() >= *CON_ROWS.get() {
                    *CON_CUR_ROW.get() = *CON_ROWS.get() - 1;
                    fb_scroll();
                }
            }
        }
    }

    fn execute(&mut self, byte: u8) {
        unsafe {
            match byte {
                b'\n' => {
                    *CON_CUR_COL.get() = 0;
                    *CON_CUR_ROW.get() += 1;
                    if *CON_CUR_ROW.get() >= *CON_ROWS.get() {
                        *CON_CUR_ROW.get() = *CON_ROWS.get() - 1;
                        fb_scroll();
                    }
                }
                b'\r' => { *CON_CUR_COL.get() = 0; }
                0x08 => {
                    if *CON_CUR_COL.get() > 0 {
                        *CON_CUR_COL.get() -= 1;
                        fb_draw_char(*CON_CUR_COL.get(), *CON_CUR_ROW.get(), b' ');
                    }
                }
                b'\t' => {
                    *CON_CUR_COL.get() = ((*CON_CUR_COL.get() + 8) & !7).min(*CON_COLS.get() - 1);
                }
                _ => {}
            }
        }
    }

    fn csi_dispatch(&mut self, params: &vte::Params, intermediates: &[u8], _ignore: bool, action: char) {
        unsafe {
            let mut iter = params.iter();
            let p0 = iter.next().map(|s| s[0] as u32).unwrap_or(0);
            let p1 = iter.next().map(|s| s[0] as u32).unwrap_or(0);
            let nparams = params.len();
            let is_dec_private = intermediates.first() == Some(&b'?');

            match action as u8 {
                b'A' => { let n = if p0 == 0 { 1 } else { p0 }; *CON_CUR_ROW.get() = (*CON_CUR_ROW.get()).saturating_sub(n); }
                b'B' => { let n = if p0 == 0 { 1 } else { p0 }; *CON_CUR_ROW.get() = (*CON_CUR_ROW.get() + n).min(*CON_ROWS.get() - 1); }
                b'C' => { let n = if p0 == 0 { 1 } else { p0 }; *CON_CUR_COL.get() = (*CON_CUR_COL.get() + n).min(*CON_COLS.get() - 1); }
                b'D' => { let n = if p0 == 0 { 1 } else { p0 }; *CON_CUR_COL.get() = (*CON_CUR_COL.get()).saturating_sub(n); }
                b'H' | b'f' => {
                    let row = if p0 == 0 { 1 } else { p0 };
                    let col = if nparams >= 2 && p1 > 0 { p1 } else { 1 };
                    *CON_CUR_ROW.get() = (row - 1).min(*CON_ROWS.get() - 1);
                    *CON_CUR_COL.get() = (col - 1).min(*CON_COLS.get() - 1);
                }
                b'J' => match p0 {
                    0 => {
                        fb_clear_region(*CON_CUR_COL.get(), *CON_COLS.get(), *CON_CUR_ROW.get(), *CON_CUR_ROW.get() + 1);
                        if *CON_CUR_ROW.get() + 1 < *CON_ROWS.get() {
                            fb_clear_region(0, *CON_COLS.get(), *CON_CUR_ROW.get() + 1, *CON_ROWS.get());
                        }
                    }
                    1 => {
                        if *CON_CUR_ROW.get() > 0 { fb_clear_region(0, *CON_COLS.get(), 0, *CON_CUR_ROW.get()); }
                        fb_clear_region(0, *CON_CUR_COL.get() + 1, *CON_CUR_ROW.get(), *CON_CUR_ROW.get() + 1);
                    }
                    2 | 3 => {
                        fb_clear_region(0, *CON_COLS.get(), 0, *CON_ROWS.get());
                        *CON_CUR_COL.get() = 0; *CON_CUR_ROW.get() = 0;
                    }
                    _ => {}
                }
                b'K' => match p0 {
                    0 => fb_clear_region(*CON_CUR_COL.get(), *CON_COLS.get(), *CON_CUR_ROW.get(), *CON_CUR_ROW.get() + 1),
                    1 => fb_clear_region(0, *CON_CUR_COL.get() + 1, *CON_CUR_ROW.get(), *CON_CUR_ROW.get() + 1),
                    2 => fb_clear_region(0, *CON_COLS.get(), *CON_CUR_ROW.get(), *CON_CUR_ROW.get() + 1),
                    _ => {}
                }
                // -------------------------------------------------------
                // DEC private modes: CSI ? Pn h (set) / CSI ? Pn l (reset)
                // -------------------------------------------------------
                b'h' | b'l' => {
                    let set = action as u8 == b'h';
                    if is_dec_private {
                        // Iterate all params — some apps send multiple modes
                        // in one sequence (e.g. CSI ?1049;25h).
                        for sub in params.iter() {
                            let mode = sub[0] as u32;
                            match mode {
                                25 => { // DECTCEM — cursor visibility
                                    CURSOR_VISIBLE = set;
                                }
                                1049 | 1047 => { // Alt screen
                                    if set && !ALT_SCREEN_ACTIVE {
                                        ALT_SAVED_COL = *CON_CUR_COL.get();
                                        ALT_SAVED_ROW = *CON_CUR_ROW.get();
                                        fb_clear_region(0, *CON_COLS.get(), 0, *CON_ROWS.get());
                                        *CON_CUR_COL.get() = 0;
                                        *CON_CUR_ROW.get() = 0;
                                        ALT_SCREEN_ACTIVE = true;
                                    } else if !set && ALT_SCREEN_ACTIVE {
                                        fb_clear_region(0, *CON_COLS.get(), 0, *CON_ROWS.get());
                                        *CON_CUR_COL.get() = ALT_SAVED_COL;
                                        *CON_CUR_ROW.get() = ALT_SAVED_ROW;
                                        ALT_SCREEN_ACTIVE = false;
                                    }
                                }
                                // Accept silently: DECCKM(?1), DECAWM(?7),
                                // att610 blink(?12), bracketed paste(?2004)
                                1 | 7 | 12 | 2004 => {}
                                _ => {} // unknown — ignore
                            }
                        }
                    }
                    // Non-private h/l (SM/RM) — accept silently
                }
                // DECSCUSR: CSI Ps SP q — cursor style
                b'q' if intermediates.first() == Some(&b' ') => {
                    let style = p0 as u8;
                    if style <= 6 {
                        CURSOR_STYLE = style;
                    }
                }
                b'm' | b'T' | b'n' | b'P' | b'L' | b'M' | b'r' => {}
                b'S' => { let n = if p0 == 0 { 1 } else { p0 }; for _ in 0..n { fb_scroll(); } }
                b's' => { *ANSI_SAVED_COL.get() = *CON_CUR_COL.get(); *ANSI_SAVED_ROW.get() = *CON_CUR_ROW.get(); }
                b'u' => { *CON_CUR_COL.get() = *ANSI_SAVED_COL.get(); *CON_CUR_ROW.get() = *ANSI_SAVED_ROW.get(); }
                b'@' | b'X' => {
                    let n = if p0 == 0 { 1 } else { p0 };
                    let end = (*CON_CUR_COL.get() + n).min(*CON_COLS.get());
                    fb_clear_region(*CON_CUR_COL.get(), end, *CON_CUR_ROW.get(), *CON_CUR_ROW.get() + 1);
                }
                b'G' => { let col = if p0 == 0 { 1 } else { p0 }; *CON_CUR_COL.get() = (col - 1).min(*CON_COLS.get() - 1); }
                b'd' => { let row = if p0 == 0 { 1 } else { p0 }; *CON_CUR_ROW.get() = (row - 1).min(*CON_ROWS.get() - 1); }
                b'E' => {
                    let n = if p0 == 0 { 1 } else { p0 };
                    *CON_CUR_COL.get() = 0;
                    *CON_CUR_ROW.get() = (*CON_CUR_ROW.get() + n).min(*CON_ROWS.get() - 1);
                }
                b'F' => {
                    let n = if p0 == 0 { 1 } else { p0 };
                    *CON_CUR_COL.get() = 0;
                    *CON_CUR_ROW.get() = (*CON_CUR_ROW.get()).saturating_sub(n);
                }
                _ => {}
            }
        }
    }

    fn esc_dispatch(&mut self, intermediates: &[u8], _ignore: bool, byte: u8) {
        unsafe {
            if !intermediates.is_empty() { return; }
            match byte {
                b'c' => { *CON_CUR_COL.get() = 0; *CON_CUR_ROW.get() = 0; fb_clear_region(0, *CON_COLS.get(), 0, *CON_ROWS.get()); }
                b'7' => { *ANSI_SAVED_COL.get() = *CON_CUR_COL.get(); *ANSI_SAVED_ROW.get() = *CON_CUR_ROW.get(); }
                b'8' => { *CON_CUR_COL.get() = *ANSI_SAVED_COL.get(); *CON_CUR_ROW.get() = *ANSI_SAVED_ROW.get(); }
                b'M' => { if *CON_CUR_ROW.get() > 0 { *CON_CUR_ROW.get() -= 1; } }
                b'D' => {
                    *CON_CUR_ROW.get() += 1;
                    if *CON_CUR_ROW.get() >= *CON_ROWS.get() { *CON_CUR_ROW.get() = *CON_ROWS.get() - 1; fb_scroll(); }
                }
                _ => {}
            }
        }
    }
}

/// Write a single character to the framebuffer console.
pub(crate) unsafe fn fb_putchar(ch: u8) {
    if *FB_PTR.get() == 0 { return; }
    // Respect the `suspend()` kill-switch: once the compositor owns the FB,
    // any further init-side rasterisation would race the repaint cycle and
    // corrupt the screen. Direct callers outside this module (boot_tests,
    // child_handler) hit this path without checking FB_ACTIVE themselves.
    if !*FB_ACTIVE.get() { return; }
    if (*VTE_PARSER.get()).is_none() {
        *VTE_PARSER.get() = Some(vte::Parser::new());
    }
    (*VTE_PARSER.get()).as_mut().unwrap().advance(&mut FbPerformer, ch);
}

